/**
 * 获取中文字符串的拼音首字母组合
 * @param {string} str 中文字符串
 * @returns {string} 拼音首字母组合（大写）
 */
function getPyInitials(str) {
  return str.split('').map(char => {
    // 如果是汉字且在映射表中，返回对应字母；否则保留原字符或忽略
    if (pinyinMap[char]) return pinyinMap[char];
    // 非汉字（如数字、字母）保留原样
    if (/[a-zA-Z0-9]/.test(char)) return char.toUpperCase();
    return '';
  }).join('');
}

