export const CHINA_PORTS = rawPorts.map(([name, lon, lat], index) => ({
  name,
  lon,
  lat,
  code: `CN${String(index + 1).padStart(4, '0')}`,  // 港口代码，格式 CN0001
  py: getPyInitials(name)                          // 拼音首字母组合
}));

/**
 * 计算两点之间的距离（单位：公里，使用 Haversine 公式）
 * @param {number} lon1 点1经度
 * @param {number} lat1 点1纬度
 * @param {number} lon2 点2经度
 * @param {number} lat2 点2纬度
 * @returns {number} 距离（公里）
 */
function getDistance(lon1, lat1, lon2, lat2) {
  const R = 6371; // 地球半径（公里）
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLon = (lon2 - lon1) * Math.PI / 180;
  const a = Math.sin(dLat / 2) * Math.sin(dLat / 2) +
            Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
            Math.sin(dLon / 2) * Math.sin(dLon / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  return R * c;
}

/**
 * 根据关键词搜索港口（匹配名称或拼音首字母）
 * @param {string} keyword - 搜索关键词（不区分大小写）
 * @returns {Promise<Array>} 匹配的港口列表
 */
export const searchPortsByKeyword = async (keyword) => {
  if (!keyword || !keyword.trim()) return [];
  const kw = keyword.trim().toLowerCase();
  return CHINA_PORTS.filter(port => 
    port.name.toLowerCase().includes(kw) || 
    port.py.toLowerCase().includes(kw)
  );
};

/**
 * 根据坐标搜索最近港口
 * @param {number} lon - 经度
 * @param {number} lat - 纬度
 * @returns {Promise<{name: string, distance: number}|null>} 如果最近港口距离 <= 30km，返回港口名和距离；否则返回 null
 */
export const searchPortsByCoord = async (lon, lat) => {
  if (lon === undefined || lat === undefined) return null;
  
  let nearest = null;
  let minDistance = Infinity;
  
  for (const port of CHINA_PORTS) {
    const distance = getDistance(lon, lat, port.lon, port.lat);
    if (distance < minDistance) {
      minDistance = distance;
      nearest = port;
    }
  }
  
  if (nearest && minDistance <= 30) {
    return { name: nearest.name, distance: minDistance };
  }
  return null;
};